package opencsv.utils;

public class CsvMapperRuntimeException extends RuntimeException {
    static final long serialVersionUID = -7034897190745166939L;

    public CsvMapperRuntimeException() {
        super();
    }

    public CsvMapperRuntimeException(java.lang.String s) {
        super(s);
    }

    public CsvMapperRuntimeException(java.lang.String s, java.lang.Throwable throwable) {
        super(s, throwable);
    }

    public CsvMapperRuntimeException(java.lang.Throwable throwable) {
        super(throwable);
    }
}
